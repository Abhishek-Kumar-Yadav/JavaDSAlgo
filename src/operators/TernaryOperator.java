package operators;

public class TernaryOperator {

	public static void main(String[] args) {
		int a=12;
		int b=35;
		int c=0;
		
		c=(a>b)?a:b;
		
// 			Ternary Operator   
//          { variable = (condition) ? expressionTrue : expressionFalse }... 
//			refer notes for more details
		
		System.out.println(c);

	}

}
